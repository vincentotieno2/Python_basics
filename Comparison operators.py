#Comparison operators
# == equal to
a=5
b=3
print("The value equals to is", a==b) #false

a=5
b=5
print("The value equals to is ",a==b) #true

#!= not equal to
m=4
n=8
print("The value equals to is", m!=n) #true

#> greater than
r=6
s=18
print("The value equals to is ", r>s) #false

#<= less than
k=13
l=8
print("The value equals to is",k<l) #false

#>= greater than or equal to
e=7
f=10
print("The value equals to is ",f>=e) #true

#<= less than or equal to
i=20
j=10
print("The value equals to is ",i<=j) #true


